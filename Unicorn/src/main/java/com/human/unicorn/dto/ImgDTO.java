	private int productNo;
	private String img;
	private int imgNo;
	
	public String getImgdetailsNo() {
		return imgdetailsNo;
	}
	public void setImgdetailsNo(String imgdetailsNo) {
		this.imgdetailsNo = imgdetailsNo;
	}
